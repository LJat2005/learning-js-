//switch statement

let day = "tuesday"


switch (day) {
    case "sunday":
        console.log("today is a holiday, i will wake up at 10:00 AM")
        break;

    case "monday":
        console.log("has to go office. and finish the assignment")
        break;

    default:
        console.log("please enter a valid day's name")
        break;

}


